/** Theme-aware colors with no Material role (income/expense semantics). */
data class FinatraExtraColors(
    val income: Color,
    val expense: Color,
)

private val LightExtra = FinatraExtraColors(income = IncomeGreenLight, expense = ExpenseRedLight)
private val DarkExtra = FinatraExtraColors(income = IncomeGreenDark, expense = ExpenseRedDark)

val LocalFinatraColors = staticCompositionLocalOf { LightExtra }

/** `FinatraTheme.income` / `FinatraTheme.expense` accessors inside composables. */
object FinatraTheme {
    val income: Color
        @Composable get() = LocalFinatraColors.current.income
    val expense: Color
        @Composable get() = LocalFinatraColors.current.expense
}

/**
 * App-wide Material 3 theme wrapper. Selects the light/dark [MaterialTheme] color scheme, installs
 * the brand [FinatraTypography] and [FinatraShapes], provides [LocalFinatraColors] (income/expense),
 * and syncs the system status/navigation bars to the background color.
 *
 * @param darkTheme defaults to the system setting.
 * @param dynamicColor opt-in Android 12+ wallpaper colors; off by default to preserve brand identity.
 */
@Composable
fun FinatraTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Brand identity is fixed to match the logo palette — dynamic color is opt-in.
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit,
) {
    val context = LocalContext.current
    val colors = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S ->
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        darkTheme -> DarkColors
        else -> LightColors
    }
    val extra = if (darkTheme) DarkExtra else LightExtra

    // Match the system bar icon contrast to the theme. The app is edge-to-edge (the bars are
    // transparent and content draws behind them), so we only set icon appearance — the deprecated
    // statusBarColor/navigationBarColor setters are intentionally not used. Skipped in @Preview.
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            val controller = WindowCompat.getInsetsController(window, view)
            controller.isAppearanceLightStatusBars = !darkTheme
            controller.isAppearanceLightNavigationBars = !darkTheme
        }
    }

    CompositionLocalProvider(LocalFinatraColors provides extra) {
        MaterialTheme(
            colorScheme = colors,
            typography = FinatraTypography,
            shapes = FinatraShapes,
            content = content,
        )
    }
}
